const STORAGE_KEY = "__dynamic_features";

export async function extractMissingFeature(body: string): Promise<string | null> {
    const m = body.match(/cannot be null:\s*([a-zA-Z0-9_]+)/);
    if (!m) return null;
    const feature = m[1];

    const current = (await localGet(STORAGE_KEY)) as Record<string, boolean> || {};
    if (!(feature in current)) {
        current[feature] = false;  // 默认 false
        await localSet(STORAGE_KEY, current);
        console.log("✅ Auto-added missing feature:", feature);
    }

    return feature;
}

export async function buildFeatures(
    apiType: "timeline" | "user" | "tweets" | "follow" | "bookmarks"
): Promise<Record<string, boolean>> {
    const dynamic = (await localGet(STORAGE_KEY)) as Record<string, boolean> || {};
    let baseFeatures;
    switch (apiType) {
        case "user":
            baseFeatures = baseFeaturesUser;
            break;
        case "tweets":
            baseFeatures = baseFeaturesTweets;
            break;
        case "follow":
            baseFeatures = baseFeaturesFollow;
            break;
        case "bookmarks":
            baseFeatures = baseFeaturesBookmarks;
            break;
        default:
            baseFeatures = baseFeaturesTimeline;
    }
    return { ...baseFeatures, ...dynamic };
}
